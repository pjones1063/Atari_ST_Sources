WORD isin(theta)
register WORD theta;
{
   theta = theta & (TWO_PI-1);
   if (theta <= TWO_PI/4)        
      theta =   sin_tab[     theta];       /* first quadrant  */
   else if (theta <= TWO_PI/2)
      theta =   sin_tab[TWO_PI/4-(theta-TWO_PI/4)];  /* second quadrant */
   else if (theta <= 3*TWO_PI/4)
      theta = -(sin_tab[     theta-TWO_PI/2]);  /* third quadrant  */
   else 
      theta = -(sin_tab[TWO_PI/4-(theta-3*TWO_PI/4)]); /* fourth quadrant */

   return(theta);
}

/*******       cosine function (cos)       *******/
/*	All this subroutine does is add TWO_PI/4 to the angle passed (the
equivelent of 90 degrees), and then call the sine function. */

WORD icos(theta)
WORD theta;
{
   WORD isin();
   return(isin(theta+TWO_PI/4));	
}


